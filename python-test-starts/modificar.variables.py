#Las variables son un espacio de almacenamiento que se utiliza para guardar datos. 
#En una variable se puede definir el precion de un producoty postriormente modificarlo.

#En el siguiente codigo muestra como una variable puede ser modificada.

precioPorducto = 150
print("El precio del producto es:", precioPorducto)
precioPorducto = 250
print("El nuevo precio del producto es:", precioPorducto)

#En este caso, la variable precioPorducto se inicializa con un valor de 150,
#y luego se modifica a 250.

#Ahora modificaremos el valor y el tipo de la variable.

precioPorducto = 150
print("El precio del producto es:", precioPorducto)
precioPorducto = 250.50
print("El nuevo precio del producto es:", precioPorducto)
#En este caso, la variable precioPorducto se inicializa con un valor entero de 150,
#y luego se modifica a un valor decimal de 250.50 tipo float.