#Tipos de variables
# Variables de tipo entero (int) que son números enteros.
# Variables de tipo flotante (float) que son números decimales.
# Variables de tipo cadena (str) que son secuencias de caracteres.
#Mostrare un ejemplo de cada tipo de variable en la siguiente actividad.

nombre = "Juan"  # Variable de tipo cadena (str)
edad = 30        # Variable de tipo entero (int)
altura = 1.75    # Variable de tipo flotante (float)
# Imprimir las variables
print("Nombre:", nombre)
print("Edad:", edad)
print("Altura:", altura)

#En caso de querer imprimer el tipo de variable, se puede usar la función type()
print("Tipo de variable 'nombre':", type(nombre))
print("Tipo de variable 'edad':", type(edad))
print("Tipo de variable 'altura':", type(altura))