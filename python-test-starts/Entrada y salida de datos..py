#Ejemplo de entrada y salida de datos:

print("Hola, ¿Cual es tu nombre?")
nombre = input() #Aqui se espera que el usuario ingrese su nombre
print("Hola " + nombre + ", ¡Bienvenido a Python!") #Aqui se muestra un mensaje de bienvenida con el nombre ingresado

#En este ejemplo, primero mostramos un mensaje pidiendo el nombre del usuario.bruno
#Luego, usamos la funcion input() para capturar el nombre ingresado por el usuario y lo almacenamos en la variable 'nombre'.    
#Finalmente, usamos la funcion print() para mostrar un mensaje de bienvenida que incluye el nombre del usuario.

